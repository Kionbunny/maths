public class ArithematicOp {
    public int addNum(int a,int b) {
       return a + b;
    }
    // all methods have to accept the params from there arguments when they are called in Main.java
    public int divNum(int a,int b) {
         return (a/b);
    }

    public int prod(int a,int b) {
        return a*b ;
    }
    public boolean product(int a,int b) {
        return (a * b) > 0;
    }
    public int areaRectangle(int height, int width) {
        return height * width;
    }
    public int sub(int a,int b) {
        return a-b;
    }

}
